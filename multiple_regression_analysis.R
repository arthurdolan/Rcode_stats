load("UN2.Rdata")
model10 <- lm(logFertility~logPPgdp, data = UN2)
model11 <- lm(logFertility~logPPgdp + Purban, data = UN2)
summary(model10)
summary(model11)
ResSumSQ10 <- sum(residuals(model10)^2)
ResSumSQ11 <- sum(residuals(model11)^2)
cat("RSS for Model 10:", ResSumSQ10, "\n")
cat("RSS for Model 11:", ResSumSQ11, "\n")
anova(model10,model11)
qf(0.95, df1 = 1, df2 = 190)
